function formatU(n) {
  // 使用紧凑货币格式显示
  const num = Number(n) || 0;
  const absNum = Math.abs(num);
  const sign = num < 0 ? '-' : '';
  
  if (absNum >= 1e15) {
    return sign + '$' + (absNum / 1e15).toFixed(0) + 'Q';
  }
  if (absNum >= 1e12) {
    return sign + '$' + (absNum / 1e12).toFixed(0) + 'T';
  }
  if (absNum >= 1e9) {
    return sign + '$' + (absNum / 1e9).toFixed(0) + 'B';
  }
  if (absNum >= 1e6) {
    return sign + '$' + (absNum / 1e6).toFixed(0) + 'M';
  }
  if (absNum >= 1e3) {
    return sign + '$' + (absNum / 1e3).toFixed(0) + 'K';
  }
  return sign + '$' + absNum.toFixed(0);
}

function formatNumber(n) {
  // 纯数字格式化，不带$符号（用于粉丝数等）
  const num = Number(n) || 0;
  const absNum = Math.abs(num);
  
  if (absNum >= 1e12) {
    return (absNum / 1e12).toFixed(1) + 'T';
  }
  if (absNum >= 1e9) {
    return (absNum / 1e9).toFixed(2) + 'B';
  }
  if (absNum >= 1e6) {
    return (absNum / 1e6).toFixed(2) + 'M';
  }
  if (absNum >= 1e3) {
    return (absNum / 1e3).toFixed(1) + 'K';
  }
  return absNum.toFixed(0);
}

