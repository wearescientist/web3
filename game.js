/** 自动决策（自动模式） */
function autoResolveDecision(decision) {
  const s = game.state;
  
  // 根据人物特性选择
  const rationality = s.rationality || 0.5;
  const riskPreference = s.riskPreference || 0.5;
  const luck = s.luck || 0.5;
  
  // 计算每个选项的权重
  const weights = decision.choices.map((c, i) => {
    // 简单启发：理性高偏好保守选项（通常是最后一个），风险偏好高偏好激进选项（通常是第一个）
    let weight = 1;
    if (i === 0) weight = riskPreference * 2 + (1 - rationality);
    else if (i === decision.choices.length - 1) weight = rationality * 2;
    else weight = 1;
    return Math.max(0.1, weight);
  });
  
  const total = weights.reduce((a, b) => a + b, 0);
  let r = Math.random() * total;
  let choiceIndex = 0;
  for (let i = 0; i < weights.length; i++) {
    r -= weights[i];
    if (r <= 0) { choiceIndex = i; break; }
  }
  
  applyDecision(decision, choiceIndex, true);
}

/** 手动决策选择 */
function resolveDecision(choiceIndex) {
  if (game.decisionTimer) {
    clearInterval(game.decisionTimer);
    game.decisionTimer = null;
  }
  
  const overlay = document.getElementById('decisionOverlay');
  overlay.classList.remove('show');
  
  applyDecision(game.pendingDecision, choiceIndex, false);
}

/** 应用决策效果 */
function applyDecision(decision, choiceIndex, isAuto) {
  const s = game.state;
  const choice = decision.choices[choiceIndex];
  
  // 记录已使用
  s.usedDecisions.push(decision.id);
  
  // 应用效果
  const result = choice.effect(s);
  
  // 记录日志
  const timeStr = `第${s.year + 1}年${s.week + 1}月`;
  const modeText = isAuto ? '(自动)' : '(手动)';
  s.eventLog.unshift({ 
    time: timeStr, 
    text: `[决策] ${decision.title}: ${choice.text} ${modeText} → ${result}`, 
    type: 'mixed', 
    netWealth: getNetWealth(s) 
  });
  
  game.pendingDecision = null;
  
  // 恢复游戏
  if (game.decisionMode === 'manual') {
    game.paused = false;
    startTickLoop();
  }
  
  updateUI();
  renderLog();
}

/** 根据人物设定计算各路径的权重（0~1），用于加权随机抽一条路径 */
function computePathWeights(traits) {
  const luck = traits.luck != null ? traits.luck : 0.5;
  const social = traits.social != null ? traits.social : 0.5;
  const rationality = traits.rationality != null ? traits.rationality : 0.5;
  const strategy = traits.strategy != null ? traits.strategy : 0.5;
  const risk = traits.riskPreference != null ? traits.riskPreference : 0.5;
  return {
    kol:    social * 0.7 + (1 - rationality) * 0.3 + luck * 0.2,
    job:    social * 0.4 + rationality * 0.5 + (1 - risk) * 0.4 + strategy * 0.2,
    trading: (1 - social) * 0.5 + (1 - rationality) * 0.3 + risk * 0.5 + (1 - strategy) * 0.4,
    launch: risk * 0.5 + social * 0.5,
    invest: rationality * 0.5 + strategy * 0.6 + social * 0.2,
    staking: rationality * 0.4 + (1 - risk) * 0.5 + (1 - social) * 0.2,
    airdrop: strategy * 0.4 + luck * 0.3 + rationality * 0.2,
  };
}

/** 按权重加权随机抽一条路径 */
function pickPathByWeights(weights) {
  const ids = PATH_IDS;
  let total = 0;
  ids.forEach(id => { total += Math.max(0, weights[id] || 0) + 0.05; });
  let r = Math.random() * total;
  for (const id of ids) {
    r -= Math.max(0, weights[id] || 0) + 0.05;
    if (r <= 0) return id;
  }
  return ids[ids.length - 1];
}

